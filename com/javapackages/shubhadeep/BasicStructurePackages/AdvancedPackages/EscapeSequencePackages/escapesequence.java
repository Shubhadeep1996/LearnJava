package com.javapackages.shubhadeep.BasicStructurePackages.AdvancedPackages.EscapeSequencePackages;

public class escapesequence {
    public static void main(String[] args) {
        System.out.println("I eat Rice");
        System.out.println("1");
        System.out.println("Shubhadeep\tis Re-Learning Java");
        System.out.println("2");
        System.out.println("Shubhadeep\nis Re-Learning Java");
        System.out.println("3");
        System.out.println("Shubhadeep\b is Re-Learning Java");
        System.out.println("4");
        System.out.println("Shubhadeep\ris Re-Learning Java");
        System.out.println("5");
        System.out.println("Shubhadeep\f is Re-Learning Java");
        System.out.println("6");
        System.out.println("Shubhadeep\'s Re-Learning Java");
        System.out.println("7");
        System.out.println("Shubhadeep\"s Re-Learning Java");
        System.out.println("8");
        System.out.println("Shubhadeep\\is Re-Learning Java");
        System.out.println("9");
        System.out.println("Shubhadeep\\u0069s Re-Learning Java");

        /*
          * Usage of escape sequences:
          * \t - Inserts a tab character. Useful for aligning text.
          * \n - Inserts a newline character. Useful for breaking lines.
          * \b - Inserts a backspace character. Useful for deleting the previous character.
          * \r - Inserts a carriage return. Moves the cursor to the beginning of the line.
          * \f - Inserts a form feed character. Useful for page breaks in text.
          * \' - Inserts a single quote character. Useful for single-quoted strings.
          * \" - Inserts a double quote character. Useful for double-quoted strings.
          * \\ - Inserts a backslash character. Useful for file paths and escaping characters.
          * \\uXXXX' - Inserts a Unicode character. Useful for including special characters.
          */
    }
}
